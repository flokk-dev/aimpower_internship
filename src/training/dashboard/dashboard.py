# IMPORT: utils
from typing import *
import time

import os
import torch

# IMPORT: data processing
from torchvision import transforms

# IMPORT: visualization
import wandb


class Dashboard:
    """
    Represents a dashboard.

    Attributes
    ----------
        _loss : List[float]
            history of the loss value during training
        _tensor_to_pil: torchvision.transforms.Transform
            transform tensor to PIL image

    Methods
    ----------
        collect_info
            Specifies the noise_scheduler to follow
        shutdown
            Shutdowns the dashboard
        update_loss
            Updates the history of loss
        upload_values
            Uploads the history of learning rate and loss
        upload_images
            Uploads examples of image generation
    """

    def __init__(
            self,
            train_id: str,
            mode="online"
    ):
        """
        Instantiates a Dashboard.

        Parameters
        ----------
            train_id : str
                id of the training
            mode : str
                WandB display mode
        """
        # Initialize the wandb entity
        os.environ["WANDB_SILENT"] = "false"
        wandb.init(entity="flokk-dev", project="aimpower_internship_test", mode=mode)

        wandb.run.name = train_id

        # Attributes
        self._loss: List[float] = list()
        self._tensor_to_pil = transforms.ToPILImage()

    @staticmethod
    def collect_info(
            model: torch.nn.Module
    ):
        """
        Specifies the noise_scheduler to follow.

        Parameters
        ----------
            model : torch.nn.Module
                noise_scheduler to follow
        """
        wandb.watch(model)

    @staticmethod
    def shutdown():
        """ Shutdowns the dashboard. """
        wandb.finish()

    def update_loss(
            self,
            loss: List[float]
    ):
        """
        Updates the history of loss.

        Parameters
        ----------
            loss : List[float]
                loss values during an epoch
        """
        # Update the loss
        current_loss = sum(loss) / len(loss)
        self._loss.append(current_loss)

    def upload_values(
            self,
            lr: float
    ):
        """
        Uploads the history of learning rate and loss.

        Parameters
        ----------
            lr : float
                learning rate value during the epoch
        """
        wandb.log({
            "loss": self._loss[-1],
            "lr": lr
        })

    def upload_images(
            self,
            log_key: str,
            tensor: torch.Tensor
    ):
        """
        Uploads examples of image generation.

        Parameters
        ----------
            log_key : str
                key with which to log the tensor
            tensor : torch.Tensor
                tensor generated by the diffusion noise_scheduler
        """
        wandb.log({
            log_key: [
                wandb.Image(self._tensor_to_pil(tensor[batch_idx]))
                for batch_idx
                in range(tensor.shape[0])
            ]
        })
        time.sleep(2)
